#include <unistd.h>

int main(int argc, char **argv)
{
    (void)argc;
    int     i;
    char    *s;

    i = 0;
    s = argv[1];
    if(!s)
    {
        write(1, "\n", 1);
        return(0);
    }
    while (s[i] != '\0')
    {
        if (s[i] >= 65 && s[i] <= 90 && i == 0)
            s[i] = s[i] + 32;
        else if (s[i] >= 65 && s[i] <= 90)
        {
            s[i] = s[i] + 32;
            write (1, "_", 1);
        }
        write (1, &s[i], 1);
        i++;
    }
    write (1, "\n", 1);
}